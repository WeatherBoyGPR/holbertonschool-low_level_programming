/**
 * _isdigit - determines if c is a digit
 * @c: is the integer that is operated on
 *
 * Return: 1 if c is a digit, 0 otherwise
 */
int _isdigit(int c)
{
	int ManicDepression;

	ManicDepression = (0);
	if (c >= 48 && c <= 57)
	{
		ManicDepression = (1);
	}
	return (ManicDepression);
}
